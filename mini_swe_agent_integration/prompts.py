    "\n"
    "检查可见 working-tree diff：\n"
    "cd \"$REPO_ROOT\" && git diff\n"
    "\n"
    "最终提交：\n"
    "echo COMPLETE_TASK_AND_SUBMIT_FINAL_OUTPUT\n"
